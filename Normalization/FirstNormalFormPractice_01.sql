--Normalization TOPIC

--Normalization is a process in database design that organizes data to minimize redundancy and dependency. The goal of normalization is to divide a database into smaller, more manageable tables while ensuring data integrity and eliminating unnecessary duplication. It helps improve database efficiency, consistency, and maintainability.
------------------------------------------------------------

--PRIMARY KEY
--A unique identifier for each row in a table.
--It must be unique and cannot be NULL.

------------------------------------------------------------

--COMPOSITE KEY
